def calculate_detection_precision(model, images, labels, iou_threshold=0.5, conf_threshold=0.25):
    """
    Calcula la precisión en detección para un conjunto de imágenes.
    
    Args:
        model: Modelo YOLOv8 cargado.
        images: Lista de rutas a las imágenes o imágenes en formato numpy array.
        labels: Lista de etiquetas reales en formato compatible.
        iou_threshold: Umbral de IoU para considerar una detección como correcta.
        conf_threshold: Umbral de confianza para filtrar predicciones.
        
    Returns:
        precision: Valor de precisión calculado.
    """
    TP = 0
    FP = 0
    total_gt = 0  # número total de objetos reales
    
    for img_path, gt_labels in zip(images, labels):
        # Realizar predicción
        results = model(img_path, conf=conf_threshold)
        detections = results[0].boxes  # cajas detectadas
        
        # Contar objetos reales
        total_gt += len(gt_labels)
        
        # Convertir detecciones a formato usable
        pred_boxes = []
        pred_scores = []
        pred_classes = []
        for box in detections:
            pred_boxes.append(box.xyxy.cpu().numpy())
            pred_scores.append(box.conf.cpu().numpy())
            pred_classes.append(box.cls.cpu().numpy())
        
        matched_gt = set()
        for pred_box, score, cls in zip(pred_boxes, pred_scores, pred_classes):
            # Buscar si hay una caja real coincidente
            ious = []
            for idx, gt in enumerate(gt_labels):
                if idx in matched_gt:
                    continue
                if gt['class'] == cls:
                    iou = compute_iou(pred_box, gt['bbox'])
                    ious.append((iou, idx))
            if ious:
                max_iou, match_idx = max(ious, key=lambda x: x[0])
                if max_iou >= iou_threshold:
                    TP += 1
                    matched_gt.add(match_idx)
                else:
                    FP += 1
            else:
                FP += 1
                
    precision = TP / (TP + FP) if (TP + FP) > 0 else 0
    return precision

def compute_iou(boxA, boxB):
    # boxA y boxB en formato [xmin, ymin, xmax, ymax]
    xA = max(boxA[0][0], boxB[0])
    yA = max(boxA[0][1], boxB[1])
    xB = min(boxA[2][0], boxB[2])
    yB = min(boxA[2][1], boxB[3])
    interArea = max(0, xB - xA + 1) * max(0, yB - yA + 1)
    boxAArea = (boxA[0][2] - boxA[0][0] + 1) * (boxA[0][3] - boxA[0][1] + 1)
    boxBArea = (boxB[2] - boxB[0] + 1) * (boxB[3] - boxB[1] + 1)
    iou = interArea / float(boxAArea + boxBArea - interArea)
    return iou

