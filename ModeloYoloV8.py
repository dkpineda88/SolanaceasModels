def compute_iou(boxes1, boxes2):
    """Calcula IoU entre dos conjuntos de cajas (Nx4 y Mx4)."""
    boxes1 = tf.expand_dims(boxes1, 1)  # Nx1x4
    boxes2 = tf.expand_dims(boxes2, 0)  # 1xMx4

    xmin1, ymin1, xmax1, ymax1 = tf.split(boxes1, 4, axis=-1)
    xmin2, ymin2, xmax2, ymax2 = tf.split(boxes2, 4, axis=-1)

    inter_xmin = tf.maximum(xmin1, xmin2)
    inter_ymin = tf.maximum(ymin1, ymin2)
    inter_xmax = tf.minimum(xmax1, xmax2)
    inter_ymax = tf.minimum(ymax1, ymax2)

    inter_area = tf.maximum(0.0, inter_xmax - inter_xmin) * tf.maximum(0.0, inter_ymax - inter_ymin)

    boxes1_area = (xmax1 - xmin1) * (ymax1 - ymin1)
    boxes2_area = (xmax2 - xmin2) * (ymax2 - ymin2)

    union_area = boxes1_area + boxes2_area - inter_area

    iou = inter_area / tf.maximum(union_area, 1e-8)
    return tf.squeeze(iou, axis=-1)  # NxM

def calculate_precision(true_boxes, true_classes, pred_boxes, pred_classes, pred_confidence, iou_threshold=0.5, confidence_threshold=0.5):
    """
    Calcula la precisión para un conjunto de predicciones y etiquetas.
    """
    true_boxes = tf.ragged.to_dense(true_boxes)
    true_classes = tf.ragged.to_dense(true_classes)
    pred_boxes = tf.ragged.to_dense(pred_boxes)
    pred_classes = tf.ragged.to_dense(pred_classes)
    pred_confidence = tf.ragged.to_dense(pred_confidence)


    # Filtrar predicciones por confianza
    mask = pred_confidence >= confidence_threshold
    pred_boxes = tf.boolean_mask(pred_boxes, mask)
    pred_classes = tf.boolean_mask(pred_classes, mask)
    pred_confidence = tf.boolean_mask(pred_confidence, mask)

    tp = 0
    fp = 0
    matched_true_indices = []

    # Para cada predicción, buscar la caja verdadera con mayor IoU
    for i in range(tf.shape(pred_boxes)[0]):
        pred_box = pred_boxes[i]
        pred_class = pred_classes[i]
        # Filtrar verdaderas por clase
        mask_true = true_classes == pred_class
        true_boxes_cls = tf.boolean_mask(true_boxes, mask_true)

        if tf.shape(true_boxes_cls)[0] == 0:
            # No hay cajas verdaderas de esa clase, es FP
            fp += 1
            continue

        ious = compute_iou(tf.expand_dims(pred_box, 0), true_boxes_cls)
        max_iou = tf.reduce_max(ious)
        max_idx = tf.argmax(ious)

        if max_iou >= iou_threshold and max_idx not in matched_true_indices:
            tp += 1
            matched_true_indices.append(max_idx.numpy())
        else:
            fp += 1

    precision = tp / (tp + fp) if (tp + fp) > 0 else 0
    return precision


